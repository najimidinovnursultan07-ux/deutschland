function mergeBanks(base: ThemeBank, advanced: ThemeBank): ThemeBank {
  const merged: ThemeBank = { ...base };
  for (const [key, pool] of Object.entries(advanced)) {
    merged[key] = pool.map(applyVerifiedPair);
  }
  return merged;
}

const BANKS: Record<TargetLanguage, ThemeBank> = {
  de: mergeBanks(DE, DE_ADVANCED),
  en: mergeBanks(EN, EN_ADVANCED),
};

export function resolveTheme(theme: string): string {
  return THEME_ALIASES[theme] ?? theme;
}

export function getThemePool(
  lang: TargetLanguage,
  theme: string
): WordSeed[] {
  const key = resolveTheme(theme);
  const pool = BANKS[lang][key] ?? BANKS[lang].greetings ?? [];
  return pool.map(applyVerifiedPair);
}
